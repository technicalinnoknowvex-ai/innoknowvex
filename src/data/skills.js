// Function to get skill description by skill name
export const getSkillDescription = (skillName) => {
  if (!skillName) return "Learn and master this industry-standard technology and skill.";
  
  // Trim and normalize the skill name
  const trimmedName = skillName.trim();
  
  // Try exact match first
  if (skillsDatabase[trimmedName]) {
    return skillsDatabase[trimmedName];
  }
  
  // Try case-insensitive match
  const lowerSkillName = trimmedName.toLowerCase();
  for (const [key, value] of Object.entries(skillsDatabase)) {
    if (key.toLowerCase() === lowerSkillName) {
      return value;
    }
  }
  
  // Try matching with ampersand variations (&amp; vs &)
  const normalizedName = trimmedName.replace(/&amp;/g, "&").toLowerCase();
  for (const [key, value] of Object.entries(skillsDatabase)) {
    if (key.replace(/&amp;/g, "&").toLowerCase() === normalizedName) {
      return value;
    }
  }
  
  // Try longest keyword match (e.g., "HTML & HTML5" matches "HTML")
  let bestMatch = null;
  let bestMatchLength = 0;
  const keywords = lowerSkillName.split(/\s+|&/);
  
  for (const [key, value] of Object.entries(skillsDatabase)) {
    const keyLower = key.toLowerCase();
    for (const keyword of keywords) {
      if (keyword.length > 2 && keyLower.includes(keyword)) {
        if (keyword.length > bestMatchLength) {
          bestMatch = value;
          bestMatchLength = keyword.length;
        }
      }
    }
  }
  
  if (bestMatch) {
    return bestMatch;
  }
  
  // Try partial match - key contains full skill name
  for (const [key, value] of Object.entries(skillsDatabase)) {
    if (key.toLowerCase().includes(lowerSkillName) || lowerSkillName.includes(key.toLowerCase())) {
      return value;
    }
  }
  
  // Default description
  return "Learn and master this industry-standard technology and skill.";
};

